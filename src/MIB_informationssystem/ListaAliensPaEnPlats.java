    private void setComboBoxValues()
    {
        String query = "SELECT Benamning FROM plats";
        
        try {
            ArrayList<String> platser = mibdb.fetchColumn(query);
            
            for(String plats : platser)
            {
                cbValjPlats.addItem(plats);
            }
            
            //cbValjPlats.setPromptText("Välj en plats");
        }
        catch (InfException ex) {
            JOptionPane.showMessageDialog(null, "Platsbenämningsdatan lyckades inte att hämtas.");
            System.out.println("Internt felmeddelande: " + ex.getMessage());
        }
        
        
        
        
        
        
        
        
    }
    
    
    
    
    
    
    
    
